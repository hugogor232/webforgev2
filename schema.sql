-- PROFILES POLICIES
CREATE POLICY "Public profiles are viewable by everyone" 
ON public.profiles FOR SELECT USING (true);

CREATE POLICY "Users can insert their own profile" 
ON public.profiles FOR INSERT WITH CHECK (auth.uid() = id);

CREATE POLICY "Users can update own profile" 
ON public.profiles FOR UPDATE USING (auth.uid() = id);

-- PROJECTS POLICIES
CREATE POLICY "Users can view own projects" 
ON public.projects FOR SELECT USING (auth.uid() = user_id);

CREATE POLICY "Public projects are viewable by everyone" 
ON public.projects FOR SELECT USING (is_public = true);

CREATE POLICY "Users can insert own projects" 
ON public.projects FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update own projects" 
ON public.projects FOR UPDATE USING (auth.uid() = user_id);

CREATE POLICY "Users can delete own projects" 
ON public.projects FOR DELETE USING (auth.uid() = user_id);

-- PROJECT FILES POLICIES
-- Access depends on the parent project ownership
CREATE POLICY "Users can view files of own projects" 
ON public.project_files FOR SELECT USING (
    EXISTS (SELECT 1 FROM public.projects WHERE id = project_files.project_id AND user_id = auth.uid())
);

CREATE POLICY "Public view files of public projects" 
ON public.project_files FOR SELECT USING (
    EXISTS (SELECT 1 FROM public.projects WHERE id = project_files.project_id AND is_public = true)
);

CREATE POLICY "Users can insert files to own projects" 
ON public.project_files FOR INSERT WITH CHECK (
    EXISTS (SELECT 1 FROM public.projects WHERE id = project_files.project_id AND user_id = auth.uid())
);

CREATE POLICY "Users can update files of own projects" 
ON public.project_files FOR UPDATE USING (
    EXISTS (SELECT 1 FROM public.projects WHERE id = project_files.project_id AND user_id = auth.uid())
);

CREATE POLICY "Users can delete files of own projects" 
ON public.project_files FOR DELETE USING (
    EXISTS (SELECT 1 FROM public.projects WHERE id = project_files.project_id AND user_id = auth.uid())
);

-- SUBSCRIPTIONS POLICIES
CREATE POLICY "Users can view own subscription" 
ON public.subscriptions FOR SELECT USING (auth.uid() = user_id);

-- SUPPORT TICKETS POLICIES
CREATE POLICY "Users can view own tickets" 
ON public.support_tickets FOR SELECT USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own tickets" 
ON public.support_tickets FOR INSERT WITH CHECK (auth.uid() = user_id);

